public class using_operators {

    public static void main(String args[]){
         System.out.println("using and & operator");
        System.out.println(5&6);

        System.out.println("using | operator");

        System.out.println(5|6);

        System.out.println("using XOR operator");

        System.out.println(6^111);

        System.out.println("using ONE's complement");
        System.out.println(~0);

        System.out.println("using the left shift");
        System.out.println(5<<2);

        System.out.println("using the right ship");
        System.out.println(6>>2);



    }
    
}
