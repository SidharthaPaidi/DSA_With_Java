public class type_promotion {
    public static void main(String args[]) {

        char a = 'a';
        char b = 'b';

        char aa = a;

        System.out.println((int)(a));
        System.out.println((int)(b));
        System.out.println(aa);
        System.out.println();

        System.out.println(b-a);

        
    }
    
}
