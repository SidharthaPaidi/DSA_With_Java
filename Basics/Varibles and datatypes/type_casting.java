public class type_casting {
    public static void main(String args[]) {

        float marks = 99.898f;
        int percent = (int)marks;

        System.out.println(percent);
        System.out.println("\n");
        
        char ch='a';
        char ch2='b';
        
        int number = ch;
        int number2 = ch2;

        System.out.println(number);
        System.out.println(number2);
    }
    
}
