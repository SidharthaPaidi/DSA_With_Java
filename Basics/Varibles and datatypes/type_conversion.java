import java.util.Scanner;

public class type_conversion {
    public static void main(String args[]) {
        int a = 10;
        float b = a;
        
        System.out.println(b);

        double c = b;

        System.out.println(c);

        Scanner sc = new Scanner(System.in);

        float z = sc.nextInt();

        System.out.println(z);


        
    }
    
}
