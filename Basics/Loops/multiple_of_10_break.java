import java.util.*;

public class multiple_of_10_break {
    public static void main(String args[]) {

    Scanner sc = new Scanner(System.in);
   
    do {
        int num = sc.nextInt();
        System.out.println(num);
        if(num%10==0){
            break;
        }
    } while (true);
    

        
    }
    
}
