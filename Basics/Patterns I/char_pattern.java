public class char_pattern {
    public static void main(String args[]) {
    
        int n = 4;
        char ch = 'A';
        for (int line = 1; line <=n; line++) {
            for (int place = 1; place <=line; place++) {
                System.out.print(ch);
                ch++;
                
            }System.out.println();
        
            
        }
    }
    
}
