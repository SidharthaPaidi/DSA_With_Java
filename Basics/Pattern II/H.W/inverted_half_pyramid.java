public class inverted_half_pyramid {
    public static void main(String args[]) {

        int n = 4;

        for (int line = 1; line <= 4; line++) {
            for (int times = 1; times <= n-line+1; times++) {
                System.out.print(times);


                
            }
            System.out.println();
        }

        
    }
    
}
